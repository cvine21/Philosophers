#include "philo.h"

int	main(int argc, char **argv)
{
	int		*argv_int;

	argv_int = parse(argc, argv);
	if (!argv_int)
		return (EXIT_FAILURE);
	if (!initialize(argc, argv_int))
		return (EXIT_FAILURE);
	return (EXIT_SUCCESS);
}
